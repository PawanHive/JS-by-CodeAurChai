const insert = document.getElementById('insert');   //we can also use (querySelector) instead of (getElementById) it will give same result

window.addEventListener('keydown',(e) => {      //(window) represent whole browser window   //(keydown) events happen anywhere in the page, as long as the window is focused.
    insert.innerHTML = `
        
        <table>
    <tr>
      <th>Key</th>
      <th>Keycode</th> 
      <th>Code</th>
    </tr>
    <tr>
      <td>${e.key === ' ' ? 'Space' : e.key}</td>
      <td>${e.keyCode}</td> 
      <td>${e.code}</td>       


    </tr>
    
  </table>
       
    `;
});


